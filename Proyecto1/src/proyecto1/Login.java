    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
    String usuario = txtUsuario.getText();
    String pass = new String(txtPassword.getPassword());

    for(int i = 0; i < Proyecto1.contadorUsuarios; i++){

        if(Proyecto1.usuarios[i].usuario.equals(usuario) &&
           Proyecto1.usuarios[i].contraseña.equals(pass)){

            String rol = Proyecto1.usuarios[i].rol;

            javax.swing.JOptionPane.showMessageDialog(this,"Bienvenido " + rol);

            if(rol.equals("ADMIN")){
                PanelAdmin panel = new PanelAdmin();
                panel.setVisible(true);
            }

            this.dispose();
            return;
        }
    }

    javax.swing.JOptionPane.showMessageDialog(this,"Usuario o contraseña incorrectos");
    }//GEN-LAST:event_btnLoginActionPerformed

